const hexToRgb = (hex: string): { r: number, g: number, b: number } => {
  const regex = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i;
  const [_, r = "0", g = "0", b = "0"] = regex.exec(hex) || [];
  return {
    r: parseInt(r, 16),
    g: parseInt(g, 16),
    b: parseInt(b, 16)
  };
}

const fmt = (n: number): string => n.toString().padStart(3, " ");

